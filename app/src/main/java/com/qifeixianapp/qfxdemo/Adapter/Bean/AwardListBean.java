package com.qifeixianapp.qfxdemo.Adapter.Bean;

public class AwardListBean {
    Integer Image;
    String title;
    String time;
    String moneny;

    public Integer getImage() {
        return Image;
    }

    public void setImage(Integer image) {
        Image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getMoneny() {
        return moneny;
    }

    public void setMoneny(String moneny) {
        this.moneny = moneny;
    }
}
